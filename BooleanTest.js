// 불린 (Boolean)
console.log(2 > 1);
console.log(2 < 1);
console.log(2 >= 1);
console.log(2 <= 1);

// 일치하는가?
console.log(2 === 2);
// 불일치하는가?
console.log(2 !== 2);

// 일치하는가?
console.log('AB' === 'AB');
// 불일치하는가?
console.log('AB' !== 'AB');

// AND 연산 (&&)
console.log(true && true)
console.log(false && true)
console.log(true && false)
console.log(false && false)
// OR 연산 (||)
console.log(true || true)
console.log(false || true)
console.log(true || false)
console.log(false || false)
// NOT 연산 (!)
console.log(!true)
console.log(!false)
