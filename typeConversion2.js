// 형 변환(Type Conversion)

// 숫자
let x = 123;
console.log(x);
console.log(typeof x) // number

// 숫자 -> 문자
console.log(String(x));
console.log(typeof String(x)) // string

// 불린
let y = true;
console.log(y);
console.log(typeof y) // boolean

// 불린 -> 문자
console.log(String(y));
console.log(typeof String(y)) // string


